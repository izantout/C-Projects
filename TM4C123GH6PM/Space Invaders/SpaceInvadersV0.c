void Init(void){ 
  uint8_t i;
	// Initialize enemy
  for(i=0;i<3;i++){
    Enemy[i].x = 25*i;
    Enemy[i].y = 10;
    Enemy[i].image = SmallEnemyPointA[i];
    Enemy[i].life = 1;
  }
	// Initialize player
	BattleShip.x = MAX_X/2;
	BattleShip.y = MAX_Y-1;
	BattleShip.image = PlayerShip0;
	BattleShip.life = 1;
	
	Laser.x = BattleShip.x;
	Laser.y = BattleShip.y+2;
	Laser.image = Laser0;
	Laser.life = 0;
	
	explosion.x = Laser.x;
	explosion.y = Laser.y;
	explosion.life = 0;
}

void Move(void){ 
  uint8_t i;
	unsigned long ADC_value;
	unsigned int playershipPosition;
  for(i=0;i<3;i++){
    if(Enemy[i].x < MAX_ENEMYX){
      Enemy[i].x += 1;
			if(Enemy[i].image == SmallEnemyPointA[i]){
					Enemy[i].image = SmallEnemyPointB[i];
			}
			else if(Enemy[i].image == SmallEnemyPointB[i]){
					Enemy[i].image = SmallEnemyPointA[i];
			}
    }else{
      Enemy[i].life = 0;
    }
  }
	
	// read ADC value for player ship
	ADC_value = ADC_In298();
	playershipPosition = ADC_value*(SCREENW-18)/4095;
	
	// update player ship
	BattleShip.x = MAX_ENEMYX - playershipPosition;
	
	// update bullet or explosion
	if(SW1Pressed && Laser.life == 0){
		Laser.x = BattleShip.x+8;
		Laser.y = BattleShip.y-2;
		Laser.life = 1;
		SW1Pressed = 0;
	}
	
	for(i=0;i<3;i++){
		if((Laser.x+1>=Enemy[i].x) && (Laser.x<=Enemy[i].x+10) && (Laser.y-9<=10) && (Laser.life) && (Enemy[i].life)){
			Enemy[i].life = 0;
			explosion.x = Enemy[i].x;
			explosion.y = Enemy[i].y;
			explosion.life = 1;
			Laser.life = 0;
			score++;
		}
	}
	
	if(Laser.life){
		if(Laser.y>2){
			Laser.y-=5;
		}
		else{
			Laser.life = 0;
		}
	}
	
	if(explosion.life){
		count++;
		if(explosion.image == SmallExplosion0){
			explosion.image = SmallExplosion1;
		}
		else{
			explosion.image = SmallExplosion0;
		}
		
		if(count == 3){
			explosion.life = 0;
			count = 0;
		}
	}
}

